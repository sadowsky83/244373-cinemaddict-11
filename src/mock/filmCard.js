const takeRandomElement = (arr) => {
  return arr[Math.floor(Math.random() * (arr.length - 1))];
};

const createRandomArr = (arr, num) => {
  let newArr = [];
  for (let i = 0; i < num; i++) {
    newArr.push(arr[Math.floor(Math.random() * (arr.length - 1))]);
  }
  return newArr;
};

const generateComment = () => {
  return {
    emoji: takeRandomElement(CommentsEmoji),
    name: takeRandomElement(CommentsUserNames),
    text: takeRandomElement(Comments),
    date: Math.floor(Math.random() * 10)
  };
};

const generateFilmCard = () => {
  return {
    title: takeRandomElement(FilmCardFilmNames),
    rating: (Math.random() * 10).toFixed(1),
    year: 1900 + (Math.floor(Math.random() * 100)),
    duration: `1h ` + (Math.floor(Math.random() * 60)) + `m`,
    poster: takeRandomElement(FilmCardsPosters),
    discription: takeRandomElement(FilmCardDiscpriptions),
    comments: new Array(Math.floor(Math.random() * 10)).fill(` `).map(generateComment),
    age: Math.floor(Math.random() * 18),
    month: takeRandomElement(FilmCardMonth),
    day: Math.floor(Math.random() * 30),
    directors: createRandomArr(CommentsUserNames, 3),
    writers: createRandomArr(CommentsUserNames, 3),
    actors: createRandomArr(CommentsUserNames, 3),
    country: takeRandomElement(FilmCardCountrys),
    gentres: createRandomArr(FilmCardGentres, 3)
  };
};

const generateFilmCards = (count) => {
  return new Array(count).fill(` `).map(generateFilmCard);
};

